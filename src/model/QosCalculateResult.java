package model;

/** 存储不划分的QOS计算结果 */
public class QosCalculateResult {
	protected String wsid;
	
	protected String rtEx;
	protected String rtEn;
	protected String rtHe;
	
	protected String re;
	
	protected String minRt;
	protected String maxRt;
	protected String pautaLeftRt;
	protected String pautaRightRt;
	
	
	public String getWsid() {
		return wsid;
	}
	public void setWsid(String wsid) {
		this.wsid = wsid;
	}
	public String getRtEx() {
		return rtEx;
	}
	public void setRtEx(String rtEx) {
		this.rtEx = rtEx;
	}
	public String getRtEn() {
		return rtEn;
	}
	public void setRtEn(String rtEn) {
		this.rtEn = rtEn;
	}
	public String getRtHe() {
		return rtHe;
	}
	public void setRtHe(String rtHe) {
		this.rtHe = rtHe;
	}
	public String getRe() {
		return re;
	}
	public void setRe(String re) {
		this.re = re;
	}
	public String getMinRt() {
		return minRt;
	}
	public void setMinRt(String minRt) {
		this.minRt = minRt;
	}
	public String getMaxRt() {
		return maxRt;
	}
	public void setMaxRt(String maxRt) {
		this.maxRt = maxRt;
	}
	public String getPautaLeftRt() {
		return pautaLeftRt;
	}
	public void setPautaLeftRt(String pautaLeftRt) {
		this.pautaLeftRt = pautaLeftRt;
	}
	public String getPautaRightRt() {
		return pautaRightRt;
	}
	public void setPautaRightRt(String pautaRightRt) {
		this.pautaRightRt = pautaRightRt;
	}
	
	
	
	


}
