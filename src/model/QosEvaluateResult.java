package model;

public class QosEvaluateResult {
	private String wsid;
	private String noArea;
	private String countryArea;
	private String provinceArea;
	
	public String getWsid() {
		return wsid;
	}
	public void setWsid(String wsid) {
		this.wsid = wsid;
	}
	public String getNoArea() {
		return noArea;
	}
	public void setNoArea(String noArea) {
		this.noArea = noArea;
	}
	public String getCountryArea() {
		return countryArea;
	}
	public void setCountryArea(String countryArea) {
		this.countryArea = countryArea;
	}
	public String getProvinceArea() {
		return provinceArea;
	}
	public void setProvinceArea(String provinceArea) {
		this.provinceArea = provinceArea;
	}

	

}
